import qrcode
import qrcode.image.svg

from components.configuration import read_config

# normal, bc
amount_types = ["100", "50"]

config = read_config()


def generate_swish_qr(name, school_class, seat, booking_type):
    amount = amount_types[booking_type]

    qr = qrcode.QRCode(
        version=1,
        error_correction=qrcode.constants.ERROR_CORRECT_H,
        box_size=10,
        border=4,
    )

    data = f"C{config['swish_phone']};{str(amount)};{name.replace(' ', '+')}+{school_class}+{'plats+' + str(seat)};0"

    qr.add_data(data)
    qr.make(fit=True)

    img = qr.make_image()

    img.save("static/temp_swish.png")
